// Assignment Operators

let r = 5;
// Equal sign is the assignment operator.

// Addition Assignment
r += 1; //r = r + 1 or, 5 + 1 in this case
console.log(r);

//Subtraction Assignment
r -= 2; // r = r - 2
console.log(r);

//Multiplication Assignment
r *= 2;
console.log(r);

//Division Assignment
r /= 1;
console.log(r);

//Exponential Assignment
r **= 2; // r squared
console.log(r);

//Remainder (modulus) Assignment
r %= 1; // r = r % 1
console.log(r);
