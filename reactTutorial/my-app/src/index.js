export default class Call extends React.Component {
  constructor(props) {
    super(props);
    this.state = [];
  }

  componentDidMount() {
    const baseUrl = "https://api.spacexdata.com/v2/rockets";
    fetch(baseUrl)
      .then((result) => {
        return result.json();
      })
      .then((json) => {
        this.setState({
          json,
        });
      });
  }

  render() {
    const rockets = this.state;
    return (
      <div>
        <h1>Space X</h1>
        <div>
          <ul>
            {console.log(this.state)}
            {this.state.map((rocket) => (
              <li key={rocket.name}>{rocket.name}</li>
            ))}
          </ul>
        </div>
      </div>
    );
  }
}

